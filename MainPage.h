#ifndef SEMESTRALKA_MAINPAGE_H
#define SEMESTRALKA_MAINPAGE_H


/** @mainpage Semestralka
 *
 *  @section intro_sec Introduction
 *
 * Sandbox game, inspired by famous game Minecraft
 *
 * Features :
 *
 * - Random generated game world
 * - Different materials placed in different zones under surface
 * - Collecting and making new items
 * - Player damage and HP recovery
 * - Breaking / placing items in game world
 * - Saving and loading game progress from save file
 * - Ability to configure game from config files
 *
 *  @section control_sec Controls
 *
 *  Controls can be customized in controls.cfg file
 *
 *  Default controls :
 *
 *  A - Move left
 *  D - Move right
 *  W - Jump
 *  Arrow left - Action in left direction
 *  Arrow right - Action in right direction
 *  Arrow up - Action in up direction
 *  Arrow down - Action in down direction
 *  Q - Cancel game or screen
 *  Enter - Confirm selected choice
 *  S - Save game
 *  B - Previous selection
 *  N - Next selection
 *  0 - 9 - Fast inventory select
 *  I - Open inventory menu
 *  C - Open crafting menu
 *  R - Remove selected stack from inventory
 *
 *  Screen specific actions :
 *
 *  Inventory :
 *
 *  - Swap two Stacks : navigate to each stack and press enter
 *
 *
 */




#endif //SEMESTRALKA_MAINPAGE_H
